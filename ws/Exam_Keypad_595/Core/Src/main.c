char Keypad_Scan(void) {
    for (int c = 0; c < 3; c++) {
        // 1. ส่ง 0 ออกไปที่หลักปัจจุบัน
        HAL_GPIO_WritePin(C_Ports[c], C_Pins[c], GPIO_PIN_RESET);

        // [TRICK 1] รอแป๊บนึงให้สัญญาณไฟฟ้าเดินทาง (สำคัญมาก!)
        // ถ้าไม่มีบรรทัดนี้ บางทีมันอ่านไม่ทัน
        HAL_Delay(2);

        for (int r = 0; r < 4; r++) {
            // 2. ถ้าอ่านแถวได้ 0 (เจอปุ่มโดนกด)
            if (HAL_GPIO_ReadPin(R_Ports[r], R_Pins[r]) == GPIO_PIN_RESET) {

                // [TRICK 2] เช็คซ้ำอีกทีเพื่อความชัวร์ (Double Check)
                HAL_Delay(20); // รอ debounce
                if (HAL_GPIO_ReadPin(R_Ports[r], R_Pins[r]) == GPIO_PIN_RESET) {

                    // ถ้ายังกดอยู่จริง ให้รอจนกว่าจะปล่อยมือ
                    while(HAL_GPIO_ReadPin(R_Ports[r], R_Pins[r]) == GPIO_PIN_RESET) {
                        HAL_Delay(10); // รอเช็คทุกๆ 10ms เพื่อไม่ให้ CPU ทำงานหนักเกิน
                    }

                    // คืนค่า 1 ให้หลักนี้ก่อนส่งค่ากลับ
                    HAL_GPIO_WritePin(C_Ports[c], C_Pins[c], GPIO_PIN_SET);
                    return keypad_map[r][c];
                }
            }
        }
        // 3. คืนค่า 1 ให้หลักนี้ก่อนไปหลักถัดไป
        HAL_GPIO_WritePin(C_Ports[c], C_Pins[c], GPIO_PIN_SET);
    }
    return 0; // ไม่มีปุ่มกด
}
